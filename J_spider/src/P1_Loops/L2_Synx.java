package P1_Loops;

public class L2_Synx {
	public static void main(String args[]) {
	//for loop
		for(int i=1; i<=10; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		//While loop
		int a=1;
		while(a<=10) {
			System.out.print(a+" ");
			a++;
		}
		System.out.println();
		
		//do-while loop
		int b = 1;
		do {
			System.out.print(b+" ");
			b++;
         }while(b<=10);
	}
}
